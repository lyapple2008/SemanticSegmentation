        kind = name[:4]
        if kind == 'conv':
            kernels, bias = weights[i][0][0][0][0]
            # matconvet: weights are [width, height, inchannel, outchannel]
            # tensorflow: weights are [height, width, inchannel, outchannel]
            kernels = np.transpose(kernels, (1, 0, 2, 3))
